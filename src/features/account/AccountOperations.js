import { useEffect, useState } from 'react';
import { useDispatch, useSelector } from 'react-redux';
import { deposit, withdraw, requestLoan, payLoan } from './accountSlice';

import Error from './Error';

function AccountOperations() {
	const [depositAmount, setDepositAmount] = useState('');
	const [withdrawalAmount, setWithdrawalAmount] = useState('');
	const [loanAmount, setLoanAmount] = useState('');
	const [loanPurpose, setLoanPurpose] = useState('');
	const [currency, setCurrency] = useState('USD');
	const [isNotEffort, setIsNotEffort] = useState(false);

	const {
		loan: currentLoan,
		loanPurpose: currentLoanPurpose,
		balance,
	} = useSelector(store => store.account);
	const dispatch = useDispatch();

	function handleDeposit() {
		if (!depositAmount) return;
		dispatch(deposit(depositAmount));
		setDepositAmount('');
	}

	function handleWithdrawal() {
		if (!withdrawalAmount) return;
		if (balance < withdrawalAmount) {
			setIsNotEffort(true);
			return;
		}
		dispatch(withdraw(withdrawalAmount));
		setWithdrawalAmount('');
	}

	function handleRequestLoan() {
		if (!loanAmount || !loanPurpose) return;
		dispatch(requestLoan(loanAmount, loanPurpose));
		setLoanAmount('');
		setLoanPurpose('');
	}

	function handlePayLoan() {
		dispatch(payLoan());
	}

	useEffect(
		function () {
			if (Number(depositAmount) === 0) setDepositAmount('');
			if (Number(withdrawalAmount) === 0) setWithdrawalAmount('');
			if (Number(loanAmount) === 0) setLoanAmount('');
		},
		[depositAmount, withdrawalAmount, loanAmount],
	);

	return (
		<div>
			<h2>Your account operations</h2>
			<div className="inputs">
				<div>
					<label>Deposit</label>
					<input
						type="number"
						value={depositAmount}
						onChange={e => setDepositAmount(+e.target.value)}
					/>
					<select
						value={currency}
						onChange={e => setCurrency(e.target.value)}
					>
						<option value="USD">US Dollar</option>
						<option value="EUR">Euro</option>
						<option value="GBP">British Pound</option>
					</select>

					<button onClick={handleDeposit}>
						Deposit {depositAmount}
					</button>
				</div>

				<div>
					<label>Withdraw</label>
					<input
						style={{ color: `${isNotEffort ? 'red' : 'black'}` }}
						type="number"
						value={withdrawalAmount}
						onChange={e => {
							setWithdrawalAmount(+e.target.value);
							setIsNotEffort(false);
						}}
					/>
					<button onClick={handleWithdrawal}>
						Withdraw {withdrawalAmount}
					</button>
					{isNotEffort && <Error>Not Enough Balance!</Error>}
				</div>

				<div>
					<label>Request loan</label>
					<input
						type="number"
						value={loanAmount}
						onChange={e => setLoanAmount(+e.target.value)}
						placeholder="Loan amount"
					/>
					<input
						value={loanPurpose}
						onChange={e => setLoanPurpose(e.target.value)}
						placeholder="Loan purpose"
					/>
					<button onClick={handleRequestLoan}>Request loan</button>
				</div>

				{Number(currentLoan) !== 0 && (
					<div>
						<span>
							Pay back ${currentLoan} ({currentLoanPurpose}){' '}
						</span>
						<button onClick={handlePayLoan}>Pay loan</button>
					</div>
				)}
			</div>
		</div>
	);
}

export default AccountOperations;
